# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution(object):
    def middleNode(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        rabbit = turtle = head
        while rabbit != None and rabbit.next != None:
            rabbit = rabbit.next.next            
            turtle = turtle.next
        return turtle